    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        DefaultTableModel model=(DefaultTableModel)jTable1.getModel();
        try {
            Class.forName("java.sql.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/d1", "root", "");
            Statement stmt = con.createStatement();
            String query = "SELECT * FROM grade1;";
            ResultSet rs = stmt.executeQuery(query);
            while (rs.next()) {
                int sno = rs.getInt("sno");
                String name = rs.getString("name");
                int age = rs.getInt("age");
                int salary = rs.getInt("salary");
                model.addRow(new Object[]{sno,name,age,salary});

            }
            rs.close();
            stmt.close();
            con.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e); }      // TODO add your handling code here:
}//GEN-LAST:event_jButton1ActionPerformed

